'''
numpy模块的矩阵计算
'''
x = [1, 2, 3, 4]
print(x * 2)
# [1, 2, 3, 4, 1, 2, 3, 4]

# 但想要的是矩阵相乘
import numpy as np
nx = np.array([1, 2, 3, 4])
ny = np.array([5, 6, 7, 8])
print(nx * 2)
# [2 4 6 8]
print(nx + 10)
# [11 12 13 14]
print(nx + ny)
# [ 6  8 10 12]
print(nx * ny)
# [ 5 12 21 32]

'''
函数封装
'''
def f(x):
    return 3 * x ** 2 - 2 * x + 7

print(f(nx))
# [ 8 15 28 47]

'''
numpy还封装了一些'universal 函数'
'''
o = np.sqrt(nx)
print(o)
# [ 1.          1.41421356  1.73205081  2.        ]
o = np.cos(nx)
print(o)
# [ 0.54030231 -0.41614684 -0.9899925  -0.65364362]

